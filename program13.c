#include<stdio.h>
void main()
{
   printf("this code will find volume of cuboid\n");
   float length, width, height;
   printf("Enter the length of the cuboid\n");
   scanf("%f",&length);
   printf("Enter the width of the cuboid\n");
   scanf("%f",&width);
   printf("Enter the height of the cuboid\n");
   scanf("%f",&height);
   printf("The volume of the cuboid is %f\n",length*width*height);
}
