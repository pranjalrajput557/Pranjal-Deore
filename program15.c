#include<stdio.h>
void main()
{
   printf("this code will find volume of cylinder\n");
   float radius, height;
   printf("Enter the radius of the cylinder\n");
   scanf("%f",&radius);
   printf("Enter the height of the cylinder\n");
   scanf("%f",&height);
   printf("The volume of the cylinder is %f\n",3.14*radius*radius*height);
}
